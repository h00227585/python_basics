if __name__ == '__main__':
    # 多个学生的个人信息列表，个人信息包括姓名、年龄和成绩
    zipped = [('张三', 18, 98), ('李四', 19, 99), ('王五', 20, 100)]
    # 提取每个学生的姓名、年龄、成绩，将它们分别归类
    names, ages, scores = zip(*zipped)
    # 打印多个学生的姓名
    print(names)   # 输出: ('张三', '李四', '王五')
    # 打印多个学生的年龄
    print(ages)    # 输出: (18, 19, 20)
    # 打印多个学生的成绩
    print(scores)  # 输出: (98, 99, 100)
